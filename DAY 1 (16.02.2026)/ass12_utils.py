def is_even(n):
    """Return True if n is even, else False."""
    return n % 2 == 0

def square(n):
    """Return the square of n."""
    return n * n

def cube(n):
    """Return the cube of n."""
    return n ** 3

def format_city(city):
    """Return city name in title case."""
    return city.title()
